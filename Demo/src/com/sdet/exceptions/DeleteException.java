package com.sdet.exceptions;

@SuppressWarnings("serial")
public class DeleteException extends Exception {

	public DeleteException() {

	}

	public DeleteException(String arg0) {
		super(arg0);

	}

	public DeleteException(Throwable arg0) {
		super(arg0);

	}

	public DeleteException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public DeleteException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);

	}

}
